#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*dest;
	int		len;

	len = ft_strlen(str);
	i = 0;
	dest = ft_calloc(len + 1, sizeof(char));
	if (!dest)
		return (NULL);
	while (str[i])
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
